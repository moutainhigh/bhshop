package com.bh.admin.vo;

public class Attachment {
	/**图片路径*/
	private String path;
	/**图片类型*/
	private String type;

	public String getPath() {
		return path;
	}
	public void setPath(String path) {
		this.path = path;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	
}
