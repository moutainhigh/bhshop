package com.bh.jd.bean.goods;

public class Check {
		
	private String skuId;
	
	private String name;
	
	private String saleState;
	
	private String isCanVAT;
	
	private String is7ToReturn;

	public String getSkuId() {
		return skuId;
	}

	public void setSkuId(String skuId) {
		this.skuId = skuId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getSaleState() {
		return saleState;
	}

	public void setSaleState(String saleState) {
		this.saleState = saleState;
	}

	public String getIsCanVAT() {
		return isCanVAT;
	}

	public void setIsCanVAT(String isCanVAT) {
		this.isCanVAT = isCanVAT;
	}

	public String getIs7ToReturn() {
		return is7ToReturn;
	}

	public void setIs7ToReturn(String is7ToReturn) {
		this.is7ToReturn = is7ToReturn;
	}
	
	
}
